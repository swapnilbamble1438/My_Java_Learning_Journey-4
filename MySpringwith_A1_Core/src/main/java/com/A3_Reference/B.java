package com.A3_Reference;


public class B {

	private int y;

	
	public B(int y) {
		super();
		this.y = y;
	}

	
	

	public B() {
		
	}




	public int getY() {
		return y;
	}




	public void setY(int y) {
		this.y = y;
	}




	@Override
	public String toString() {
		return "B [y=" + y + "]";
	}
	
	
	
}
